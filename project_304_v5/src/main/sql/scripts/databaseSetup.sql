CREATE TABLE Financial_Advisor (FID INTEGER, name CHAR(25), PRIMARY KEY(fid))
CREATE TABLE Investor (investorID INTEGER, fid INTEGER, name CHAR(25), PRIMARY KEY (investorID), FOREIGN KEY (fid) REFERENCES Financial_Advisor ON DELETE SET NULL)
CREATE TABLE PrimaryInvestor (investorID INTEGER, netWorth INTEGER, PRIMARY KEY (investorID), FOREIGN KEY (investorID) REFERENCES Investor)
CREATE TABLE SecondaryInvestor (investorID INTEGER, riskPreference INTEGER, PRIMARY KEY (investorID), FOREIGN KEY (investorID) REFERENCES Investor)
CREATE TABLE Institution (institutionName CHAR(20), PRIMARY KEY(institutionName))
CREATE TABLE Education (investorID INTEGER, institutionName CHAR(20), PRIMARY KEY(investorID,institutionName), FOREIGN KEY (institutionName) REFERENCES Institution, FOREIGN KEY (investorID) REFERENCES Investor)
CREATE TABLE Currency (currencyName CHAR(25), PRIMARY KEY(currencyName))
CREATE TABLE Exchange (exchangeName CHAR(25), PRIMARY KEY(exchangeName))
CREATE TABLE Company_Name(cid INTEGER, cName CHAR(25), PRIMARY KEY(cid))
CREATE TABLE Company_Address (cid INTEGER, Address Char(50), PRIMARY KEY(cid))
CREATE TABLE FinancialInstrument (cid INTEGER, type CHAR(25), PRIMARY KEY(cid, type), FOREIGN KEY(cid) REFERENCES Company_Name ON DELETE CASCADE)
CREATE TABLE InvestingGroup (igName CHAR(25), Branch CHAR(25), Address varchar(50), PRIMARY KEY(igName))
CREATE TABLE InvestorJoinsInvestingGroup (investorID INTEGER, igName CHAR(25), PRIMARY KEY(investorID, igName), FOREIGN KEY(investorID) REFERENCES Investor, FOREIGN KEY(igName) REFERENCES InvestingGroup)
CREATE TABLE MakesDirectInvestment (investorID INTEGER, equity numeric, amountInvested numeric, cid INTEGER, PRIMARY KEY(investorID, cid), FOREIGN KEY(investorID) REFERENCES Investor, FOREIGN KEY(cid) REFERENCES Company_Name)
CREATE TABLE PurchasePrice (Quantity INTEGER, SharePrice INTEGER, TransactionPrice INTEGER, PRIMARY KEY(Quantity, SharePrice))
CREATE TABLE TransactionBasicData (transactionID INTEGER, cid INTEGER, type CHAR(25), exchangeName CHAR(25), currencyName CHAR(25), PRIMARY KEY(transactionID), FOREIGN KEY(cid, type) REFERENCES FinancialInstrument ON DELETE SET NULL, FOREIGN KEY(exchangeName) REFERENCES Exchange, FOREIGN KEY(currencyName) REFERENCES Currency)
CREATE TABLE QuantityOfPurchase (Quantity INTEGER, transactionID INTEGER, PRIMARY KEY(transactionID), FOREIGN KEY(transactionID) REFERENCES TransactionBasicData)
CREATE TABLE PriceOfIndividualStock (SharePrice numeric, transactionID INTEGER, PRIMARY KEY(transactionID), FOREIGN KEY(transactionID) REFERENCES TransactionBasicData)
CREATE TABLE MakesTransaction (investorID INTEGER, transactionID INTEGER, PRIMARY KEY(investorID, transactionID), FOREIGN KEY(investorID) REFERENCES Investor, FOREIGN KEY(transactionID) REFERENCES TransactionBasicData)
CREATE TABLE DateOfPurchase (DateOfPurchase CHAR(10), transactionID INTEGER, PRIMARY KEY(transactionID), FOREIGN KEY (transactionID) REFERENCES TransactionBasicData)
